/** Get skills available to a class at a given level */
export function getClassSkills(className: string, level: number): SkillDefinition[] {
  return Object.values(SKILL_DEFS).filter(
    (s) => (s.className === className || s.className === 'all') && s.levelReq <= level,
  );
}
